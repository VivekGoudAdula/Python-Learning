# WHILE LOOPS
# While loop will continue to execute as long as the condition is true

i=1
while i<5:
    print(i)
    i+=1

# # BREAK STATEMENT
while i<10:
    i+=1
    if i ==5:
        break
    print(i)

# CONTINUE STATEMENT
a = 1
while a<5:
    a+=1
    if a == 2:
        continue
    print(a)

